# 仅 VPR 额外 XOR 所需；KGM/KGMA 不得使用此密钥
KGM_VPR_KEY = bytes([0x25, 0xDF, 0xE8, 0xA6, 0x75, 0x1E, 0x75, 0x0E, 0x2F, 0x80, 0xF3, 0x2D, 0xB8, 0xB6, 0xE3, 0x11, 0x00])


def _decrypt_kgm_payload(cipher_data: bytes, core_key: bytes, vpr_key: bytes | None) -> bytes:
    """流式解密 KGM/VPR 载荷（encryption version 3）。

    重要：vpr_key 只在 VPR 文件时传入；KGM/KGMA 必须为 None，
    否则会二次 XOR 导致明文损坏。
    """
    t1 = KGM_TABLE1
    t2 = KGM_TABLE2
    tv2 = KGM_TABLEV2
    out = bytearray(len(cipher_data))
    keysize = 17

    for idx, byte in enumerate(cipher_data):
        # 密文预变换
        b = byte ^ ((byte & 0x0F) << 4)

        # 位置相关掩码
        i = idx >> 4
        value = 0
        while i >= 17:
            value ^= t1[i % 272]
            i >>= 4
            value ^= t2[i % 272]
            i >>= 4
        msb = value ^ tv2[idx % 272]

        # 与核心密钥混合，再做半字节变换得到密钥流字节
        msb ^= core_key[idx % keysize]
        plain = b ^ (msb ^ ((msb & 0x0F) << 4))

        # 仅 VPR 需要额外 XOR
        if vpr_key is not None:
            plain ^= vpr_key[idx % keysize]

        out[idx] = plain & 0xFF

    return bytes(out)


def decrypt_kgm_family_file(src_path: Path, output_dir: Path) -> str:
    """解密 .kgm / .kgma / .vpr，返回输出文件名。"""
    src_path = Path(src_path)
    output_dir = Path(output_dir)
    ext = src_path.suffix.lower()
    if ext not in (".kgm", ".kgma", ".vpr"):
        raise ValueError(f"Unsupported format: {ext}")

    with open(src_path, "rb") as f:
        header = f.read(16)
        if header == VPR_MAGIC:
            is_vpr = True
        elif header == KGM_MAGIC:
            is_vpr = False
        else:
            raise ValueError(f"Invalid KGM/VPR header in {src_path.name}")

        if len(header) < 16:
            raise ValueError(f"File too short: {src_path.name}")

        cipher_data_offset = int.from_bytes(f.read(4), "little")
        encryption_version = int.from_bytes(f.read(4), "little")
        _core_key_slot = int.from_bytes(f.read(4), "little")
        core_key_test_data = f.read(16)
        _master_key = f.read(16)

        if encryption_version != 3:
            raise ValueError(
                f"Unsupported KGM encryption version {encryption_version} in {src_path.name} "
                f"(only version 3 is supported)"
            )
        if len(core_key_test_data) != 16:
            raise ValueError(f"Incomplete KGM header in {src_path.name}")

        f.seek(cipher_data_offset)
        cipher_data = f.read()

    # 核心密钥 = 文件内 16 字节测试数据 + 尾部 0x00
    core_key = core_key_test_data + b"\x00"
    # 关键修复点：KGM 不传 vpr_key；仅 VPR 使用
    vpr_key = KGM_VPR_KEY if is_vpr else None
    plain = _decrypt_kgm_payload(cipher_data, core_key, vpr_key)

    out_ext = sniff_audio_ext(plain[:16])
    if not out_ext:
        raise ValueError(
            f"Decrypted data from {src_path.name} is not a recognized audio format "
            f"(header={plain[:8].hex()})"
        )

    out_name = src_path.stem + out_ext
    out_path = output_dir / out_name
    out_path.write_bytes(plain)
    return out_name


